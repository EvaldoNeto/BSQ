#include "../includes/bsq.h"

void	ft_putnbr(int num)
{
	if (num >= 0 && num <= 9)
		ft_putchar(num + '0');
	if (num > 9)
	{
		ft_putnbr(num / 10);
		ft_putnbr(num % 10);
	}
}
